"""
Declara variables de diferentes tipos (int, float, str, bool) y muestra sus tipos con type().
"""

def main():
    entero = 10
    flotante = 3.14
    texto = "Hola"
    booleano = True

    print(entero, type(entero))
    print(flotante, type(flotante))
    print(texto, type(texto))
    print(booleano, type(booleano))

if __name__ == "__main__":
    main()
