/*
 * 🧩 Enunciado:
 * Declara dos números y muestra su suma, resta, multiplicación, división y módulo.
 */

fun main() {
    val a = 10
    val b = 3

    println("Suma: ${a + b}")
    println("Resta: ${a - b}")
    println("Multiplicación: ${a * b}")
    println("División: ${a.toDouble() / b}")
    println("Módulo: ${a % b}")
}
